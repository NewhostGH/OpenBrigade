    $today = now()->toDateString();
    $soon = now()->addDays(30)->toDateString();
    $expCard = function ($date, string $label, string $icon) use ($today, $soon) {
        if (!$date) {
            return [
                'label' => $label,
                'icon' => $icon,
                'text' => '—',
                'color' => '#94a3b8',
                'bg' => '#f8fafc',
                'sub' => ''
            ];
        }
        $ds = $date->toDateString();
        $diff = now()->diffInDays($date, false);
        $sub = $diff < 0
            ? __('vehicle.hint_expired', ['days' => abs((int) $diff)])
            : __('vehicle.hint_in', ['days' => (int) $diff]);
        if ($ds < $today) {
            return [
                'label' => $label,
                'icon' => $icon,
                'text' => $date->format('d/m/Y'),
                'color' => '#dc2626',
                'bg' => '#fff1f2',
                'sub' => $sub
            ];
        }
        if ($ds <= $soon) {
            return [
                'label' => $label,
                'icon' => $icon,
                'text' => $date->format('d/m/Y'),
                'color' => '#d97706',
                'bg' => '#fffbeb',
                'sub' => $sub
            ];
        }
        return [
            'label' => $label,
            'icon' => $icon,
            'text' => $date->format('d/m/Y'),
            'color' => '#16a34a',
            'bg' => '#f0fdf4',
            'sub' => $sub
        ];
    };

